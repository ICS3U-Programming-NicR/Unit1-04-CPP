#include <iostream>
#include <cmath>
using std::cout;


int main() {
    cout << "8 + 3 :" << 8 + 3;
    cout << "\n8 - 3 :" << 8 - 3;
    cout << "\n8 / 3 :" << 8 / 3;
    cout << "\n8 * 3 :" << 8 * 3;
    cout << "\n8 ** 3 :" << std::pow(8, 3);
    cout << "\n8 ** 1/3 :" << pow(8, 1/3);
}
